"""
CivicMind AI - Prediction Router

This module exposes HTTP endpoints for the Prediction Agent.

PURPOSE:
--------
The Prediction Agent performs time-series forecasting on structured datasets.
It uses machine learning models to predict future values based on historical data.

CURRENT MODEL:
--------------
- Linear Regression (V1)

FUTURE MODELS:
--------------
- Random Forest
- XGBoost
- Prophet
- LSTM

API FLOW:
----------
Client Request
      ↓
Prediction Router
      ↓
Prediction Agent
      ↓
ML Model
      ↓
Forecast Response
"""

from fastapi import APIRouter, HTTPException, status

# Prediction Agent business logic
from agents.prediction.prediction_agent import PredictionAgent

# Request and Response schemas
from agents.prediction.schemas import (
    
    PredictionResponse
)
from fastapi import UploadFile, File, Form

import shutil
from pathlib import Path
# =============================================================================
# ROUTER INITIALIZATION
# =============================================================================
# APIRouter helps organize endpoints into logical modules.
# All prediction-related routes will belong to this router.

router = APIRouter(
    prefix="/prediction",
    tags=["Prediction"]
)

# =============================================================================
# PREDICTION AGENT INSTANCE
# =============================================================================
# Create a single PredictionAgent instance.
# This follows the same architecture used for:
# - Coordinator Agent
# - Analytics Agent
#
# Benefits:
# - Avoid repeated object creation
# - Cleaner architecture
# - Easier future dependency injection

prediction_agent = PredictionAgent()


# =============================================================================
# FORECAST ENDPOINT
# =============================================================================

@router.post(
    "/forecast",
    response_model=PredictionResponse,
    status_code=status.HTTP_200_OK,
    summary="Generate future predictions",
    description=(
        "Loads a dataset, trains a forecasting model, "
        "and predicts future values for a selected target column."
    )
)
async def forecast(
    file: UploadFile = File(...),
    target_column: str = Form(...),
    forecast_days: int = Form(...)
) -> PredictionResponse:
    """
    Generate future forecasts from historical data.

    Request Example:
    ----------------
    {
        "file_path": "../datasets/aqi_forecast.csv",
        "target_column": "aqi",
        "forecast_days": 7
    }

    Response Example:
    -----------------
    {
        "file_name": "aqi_forecast.csv",
        "target_column": "aqi",
        "forecast_days": 7,
        "predictions": [
            185.2,
            191.8,
            198.4
        ],
        "model_used": "LinearRegression"
    }

    Parameters:
    -----------
    request:
        PredictionRequest object containing:
        - file_path
        - target_column
        - forecast_days

    Returns:
    --------
    PredictionResponse
        Forecast results generated by the Prediction Agent.

    Raises:
    -------
    HTTPException (500)
        Returned when:
        - Dataset cannot be loaded
        - Target column is missing
        - Target column is not numeric
        - Model training fails
    """

    try:

        # Save the uploaded file to a temporary location
        uploads_dir = Path("uploads")
        uploads_dir.mkdir(exist_ok=True)

        file_path = uploads_dir / file.filename

        with open(file_path, "wb") as buffer:
            shutil.copyfileobj(file.file, buffer)

        # Delegate forecasting logic to Prediction Agent.
        # The router only handles HTTP concerns.
        return prediction_agent.predict(
            file_path=str(file_path),
            target_column=target_column,
            forecast_days=forecast_days
        )

    except Exception as e:
        # Convert internal Python exceptions into
        # API-friendly HTTP responses.
        raise HTTPException(
            status_code=status.HTTP_500_INTERNAL_SERVER_ERROR,
            detail=str(e)
        )  